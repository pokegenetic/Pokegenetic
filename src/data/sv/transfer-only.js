// Helper function to check if a Pokémon is transfer-only
export function isTransferOnly(name, isShiny = false) {
    return transferOnlyPokemon.some(pokemon => pokemon.name === name && pokemon.isShiny === isShiny);
}
// Helper function to get all transfer-only Pokémon names
export function getTransferOnlyNames() {
    return [...new Set(transferOnlyPokemon.map(pokemon => pokemon.name))];
}
// Helper function to get transfer-only Pokémon by shiny status
export function getTransferOnlyByShiny(isShiny) {
    return transferOnlyPokemon.filter(pokemon => pokemon.isShiny === isShiny);
}
